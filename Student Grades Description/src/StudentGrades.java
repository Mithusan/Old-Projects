    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        //TRY CATCH
        //Try
        try{
            //VARIABLES
            //Student Names
            String firstName = FirstNameInput.getText();//First Name of Student
            String lastName = LastNameInput.getText();//Last Name of Student
            
            //Test Marks
            double test1 = Double.parseDouble(Test1Input.getText());//First Test Mark 
            double test2 = Double.parseDouble(Test2Input.getText());//Second Test Mark
            double test3 = Double.parseDouble(Test3Input.getText());//Third Test Mark
            double test4 = Double.parseDouble(Test4Input.getText());//Forth Test Mark
            
            if(Pattern.matches("[a-zA-Z]+",firstName)){
                if(Pattern.matches("[a-zA-Z]+",lastName)){
                    if ((0 <= test1 && test1 <= 100) && (0 <= test2 && test2 <= 100) &&
                            (0 <= test3 && test3 <= 100) && (0 <= test4 && test4 <= 100)){
                        //Adds Student's full name to 'fullName' array to be later Used
                        fullName.add(firstName + " " + lastName);
                        
                        //Enables These options to be used if there is atleast one student
                        if (fullName.size() > 0){
                            StudentAverage.setEnabled(true);
                            CourseAverage.setEnabled(true);
                            
                            //Prevents IndexOutBoundsException
                            ListButton.setEnabled(true);
                        }
                        
                        //Adds Marks to the 2D Array
                        mark[row][0] = test1;//First Mark
                        mark[row][1] = test2;//Second Mark
                        mark[row][2] = test3;//Third Mark
                        mark[row][3] = test4;//Forth Mark

                        //Adds 1 to variable Row
                        //Prepares variable for next time user clicks add button
                        //Effects 2D array > Row
                        row++;
                        //Makes error output empty
                        ErrorOutput.setText("");
                    }//if ((0 <= test1 && test1 <= 100) && (0 <= test2 && test2 <= 100) 
                     //&& (0 <= test3 && test3 <= 100) && (0 <= test4 && test4 <= 100))
                    else{
                        //Error Output
                        ErrorOutput.setText("Please Enter a Mark from 0-100%");
                    }//End of else

                    //Runs code if Number of Students is equal to the maximum
                    //give amount of 15
                    if (fullName.size() == 15){
                            AddButton.setEnabled(false);//Disables ability to Add More Students
                            //Error Output
                            ErrorOutput.setText("You Have Reached the Maximum Amount of Students!");
                    }//if (fullName.size() == 15)
                    
                    //LIST BUTTON
                    //Adds To ArrayList to be Used Later
                    //Displays Students Fullname and all their marks
                    Student.add(fullName.get(i) + ":\n " + mark[i][0]+ "%, " + 
                           mark[i][1]+ "%, "+ mark[i][2]+ "%, "+ mark[i][3] + "%");
                    
                    //STUDENT AVERAGE BUTTON
                    studentAverage.add(fullName.get(i) + "\nhas a " + ((mark[i][0] + 
                            mark[i][1] + mark[i][2] + mark[i][3]) / 4) + "% average");
                    
                    courseAverage1.add(fullName.get(i) + ": " + mark[i][0] + "");
                    courseAverage2.add(fullName.get(i) + ": " + mark[i][1] + "");
                    courseAverage3.add(fullName.get(i) + ": " + mark[i][2] + "");
                    courseAverage4.add(fullName.get(i) + ": " + mark[i][3] + "");
                    
                    i++;
                    
                }//if(Pattern.matches("[a-zA-Z]+",lastName))
                else{
                    //Error Output
                    ErrorOutput.setText("Please only input letters for Names");   
                }//End of else
            }//if(Pattern.matches("[a-zA-Z]+",firstName))
            else{
                //Error Output
                ErrorOutput.setText("Please only input letters for Names");  
            }//End of else
        }//End of Try
        //Catch
        catch(NumberFormatException e){//Only accepts Numbers
            //Error Output
            ErrorOutput.setText("Please Input in Correct Format!");
        }//End of Catch
    }//GEN-LAST:event_AddButtonActionPerformed

