    private void CourseAverageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CourseAverageActionPerformed
        double course1 = 0;
        double course2 = 0;
        double course3 = 0;
        double course4 = 0;
        
        for (int i = 0; i < fullName.size(); i++){
            course1 += mark[i][0];
            course2 += mark[i][1];
            course3 += mark[i][2];
            course4 += mark[i][3];
        }
        
        course1 = course1 / fullName.size();
        course2 = course2 / fullName.size();
        course3 = course3 / fullName.size();
        course4 = course4 / fullName.size();
        
        String RoundedCourse1 = String.format("%.2f", course1);
        String RoundedCourse2 = String.format("%.2f", course2);
        String RoundedCourse3 = String.format("%.2f", course3);
        String RoundedCourse4 = String.format("%.2f", course4);
        
        AverageOutput.setText("The Course Average for the first Course is:\n" + RoundedCourse1 + "%!\n" + courseAverage1 + "\n\n" +
               "The Course Average for the second Course is:\n" + RoundedCourse2 + "%!\n" + courseAverage2 + "\n\n" +
               "The Course Average for the third Course is:\n" + RoundedCourse3 + "%!\n" + courseAverage3 + "\n\n" +
               "The Course Average for the forth Course is:\n" + RoundedCourse4 + "%!\n" + courseAverage4);
    }//GEN-LAST:event_CourseAverageActionPerformed

